a = int(input("Digite um valor X: "))
b = int(input("Digite um valor Y: "))

adicao = int(a) + int(b)
subtracao = int(a) - int(b)
divisao = int(a) / int(b)
multiplicao = int(a) * int(b)

print(a, " + ", b, " = ", adicao)
print(a, " - ", b, " = ", subtracao)
print(a, " / ", b, " = ", divisao)
print(a, " * ", b, " = ", multiplicao)